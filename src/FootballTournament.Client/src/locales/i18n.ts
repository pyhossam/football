function getInitialLanguage() {
  const stored = localStorage.getItem('football-language');
  if (!stored) {
    return 'ar';
  }

  try {
    return JSON.parse(stored)?.state?.language || 'ar';
  } catch {
    return 'ar';
  }
}

void i18n.use(initReactI18next).init({
  resources,
  lng: getInitialLanguage(),
  fallbackLng: 'ar',
  interpolation: {
    escapeValue: false,
  },
});

